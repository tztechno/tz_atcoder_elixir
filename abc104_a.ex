defmodule Main do
  def main do
    [n_str] =
      IO.read(:line)
      |> String.trim
      |> String.split(" ")

    n = String.to_integer(n_str)

    if n < 1200 do
      IO.puts("ABC")
    else
      if n < 2800 do
        IO.puts("ARC")
      else
        IO.puts("AGC")
      end
    end
  end
end
