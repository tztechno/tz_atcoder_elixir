[my ac]
defmodule Main do
  def main do
    [n] =
      IO.read(:line)
      |> String.trim
      |> String.split(" ")
      |> Enum.map(&String.to_integer/1)

    if rem(n, 2) == 0 do
      IO.puts(n)
    else
      IO.puts(n * 2)
    end
  end
end
