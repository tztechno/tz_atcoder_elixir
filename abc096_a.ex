defmodule Main do
  def main do
    [a, b] =
      IO.read(:line)
      |> String.trim
      |> String.split(" ")
    a = String.to_integer(a)
    b = String.to_integer(b)
    if (a <= b) do
    	IO.puts(a)
    else
    	IO.puts(a - 1)
    end
  end
end
