defmodule Main do
  def main do
    s = String.trim(IO.gets(""))

    if String.at(s, 0) != String.at(s, 1) and
         String.at(s, 1) != String.at(s, 2) and
         String.at(s, 2) != String.at(s, 0) do
      IO.puts("Yes")
    else
      IO.puts("No")
    end
  end
end
